function formatLocal(date: Date): string {
  // แปลงเป็น YYYY-MM-DD HH:mm ตาม local timezone
  return date.toLocaleString('sv-SE', { timeZone: 'Asia/Bangkok' }).replace('T', ' ');
}

export const createSample = (h: number[], q: number[]): Sample[] => {
  const samples: Sample[] = [];
  const start = new Date('2025-10-01T01:00:00');

  for (let i = 0; i < h.length; i++) {
    const t = new Date(start.getTime() + i * 3600 * 1000);
    samples.push({
      t: t.toISOString(), // ได้ string "2025-10-01 01:00"
      h: h[i],
      q: q[i],
    });
  }

  return samples;
};

